package com.example.user.magnant.emergency;

public class EmergencyItem {
    private String head;
    private String desc;

    public EmergencyItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
